#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const read=async p=>JSON.parse(await fs.readFile(path.join(root,p),'utf8'));
const fail=m=>{throw new Error(m)};
const manifest=await read('data/content-manifest.json');
const campaign=await read('data/campaigns/babylon/campaign.json');
const story=await read('data/cards/babylon/story.json');
const archive=await read('data/cards/babylon/archive.json');
const lessons=await read('data/lessons/babylon/campaign.json');
const quizzes=await read('data/quizzes/babylon/campaign.json');
const pools=await read('data/campaigns/babylon/pools.json');
const stories=await read('data/stories/babylon/personal.json');
const map=await read('data/maps/babylon.json');
const bronze=await read('data/maps/bronze-world.json');
const catalog=await read('data/world/campaigns.json');
const js=await fs.readFile(path.join(root,'js/features/v3-1-babylon.js'),'utf8');
manifest.version==='3.5.1'||fail('manifest version');
campaign.chapters.length===8||fail('chapters');campaign.nodes.length===48||fail('missions');
story.length===64||fail('story cards');archive.length===32||fail('archive cards');
Object.keys(lessons).length===48||fail('lessons');Object.keys(quizzes).length===12||fail('quizzes');
pools.pools.length===8||fail('pools');Object.keys(stories).length===8||fail('personal stories');
campaign.nodes.find(x=>x.id==='BAB_08_06')?.campaignExamModules?.length===4||fail('campaign exam modules');
campaign.eraLayer.regions.length===6||fail('bronze regions');campaign.eraLayer.parallelTimeline.length>=8||fail('parallel timeline');
Object.keys(map.points).length>=18||fail('campaign map');Object.keys(bronze.points).length>=7||fail('bronze map');
catalog.find(x=>x.id==='BABYLON_OLD')?.status==='PLAYABLE'||fail('world catalog status');
js.includes('focusBronzeRegion')||fail('region focus');js.includes('renderBabylonExam')||fail('exam ui');
console.log('✓ v3.1 Babylon and Bronze Age smoke passed');
